<h2>RAMBLIN BIO:</h2>
  <p>The Rollin Ramblers are a buckshot blast of outlaw country mixed with old school honky tonk going all the way back to the 1950's.
  Born in a dusty garage in Pico Rivera in 2018 they fine tuned their sound by playing 100's of gigs across all of Southern California: 
  Sparkling telecasters through vintage tube driven amps, pedal steels crying and sobbing heartbroken ghosts into the bottom of yet another empty shotglass.
  Backbeats for two-steppin, or bar fightin.  Pick your poison.  Their lyrics are about freedom, dirtnaps under the moon, cheap whisky and pills and revved up V8
  engines. The ghosts of Bakersfield sharing a drink with some punk rock spirits in a haunted honky tonk.  Someone said they saw a flaming tumbleweed roll out and disappear into the dark desert night at one of their shows.
  </p>
